def length_ciagu(param):
    x = len(param)
    return x


def lower_ciagu(param):
    x = param.lower()
    return x


def upper_ciagu(param):
    x = param.upper()
    return x


def capitalize_ciagu(param):
    x = param.capitalize()
    return x


def index_ciagu(substr, string):
    x = string.index(substr)
    return x
